// MathUtils.cpp

#include "MathUtils.h"

using namespace MathUtilsLib;

int MathUtils::Add(int a, int b) {
    return a + b;
}

double MathUtils::Add(double a, double b) {
    return a + b;
}

int MathUtils::Multiply(int a, int b) {
    return a * b;
}

double MathUtils::Multiply(double a, double b) {
    return a * b;
}
